function enhanceSelect(sel) {
    if (!sel || sel._enhanced) return;
    var wrap = document.createElement('div');
    wrap.className = 'nice-select';
    sel.parentNode.insertBefore(wrap, sel);
    wrap.appendChild(sel);
    sel.classList.add('nice-select__native');
    var btn = document.createElement('button');
    btn.type = 'button';
    btn.className = 'nice-select__btn';
    btn.innerHTML = '<span></span><svg viewBox="0 0 24 24" width="18" height="18" fill="none" stroke="currentColor" stroke-width="2"><polyline points="6 9 12 15 18 9"/></svg>';
    var menu = document.createElement('div');
    menu.className = 'nice-select__menu';
    wrap.appendChild(btn);
    wrap.appendChild(menu);
    sel._enhanced = { wrap: wrap, btn: btn, menu: menu };
    btn.addEventListener('click', function(e) {
        e.stopPropagation();
        document.querySelectorAll('.nice-select.open').forEach(function(w) { if (w !== wrap) w.classList.remove('open'); });
        wrap.classList.toggle('open');
    });
    document.addEventListener('click', function(e) { if (!wrap.contains(e.target)) wrap.classList.remove('open'); });
    syncNiceSelect(sel);
}
function syncNiceSelect(sel) {
    var e = sel && sel._enhanced;
    if (!e) return;
    e.menu.innerHTML = '';
    var span = e.btn.querySelector('span');
    var selOpt = sel.options[sel.selectedIndex];
    var isPlaceholder = !sel.value || (selOpt && selOpt.disabled);
    e.btn.classList.toggle('nice-select__btn--placeholder', !!isPlaceholder);
    span.textContent = selOpt ? selOpt.text : '';
    for (var i = 0; i < sel.options.length; i++) {
        var o = sel.options[i];
        if (o.disabled) continue;
        var item = document.createElement('div');
        item.className = 'nice-select__opt' + (o.value === sel.value && !isPlaceholder ? ' nice-select__opt--active' : '');
        item.textContent = o.text;
        (function(val) {
            item.addEventListener('click', function() {
                sel.value = val;
                sel.dispatchEvent(new Event('change', { bubbles: true }));
                syncNiceSelect(sel);
                e.wrap.classList.remove('open');
            });
        })(o.value);
        e.menu.appendChild(item);
    }
}

// ===== PRICE CALCULATOR =====
var selectedFlavourPrice = 0;
var basePriceMini = 30;   // price for 5"
var basePriceMaxi = 35;   // price for 6"
var PRICE_PER_INCH = 5;   // each extra inch beyond 5"

function getInches() {
    var size = cakeSizeSelect.value;
    if (size === 'mini') return 5;
    if (size === 'maxi') return 6;
    if (size === 'custom') {
        var d = parseFloat(customDiameterInput.value);
        return isNaN(d) ? null : d;
    }
    return 5;
}

function sizePrice() {
    var size = cakeSizeSelect.value;
    if (!size) return null;
    if (size === 'mini') return basePriceMini;
    if (size === 'maxi') return basePriceMaxi;
    if (size.charAt(0) === 'g') {
        var gs = _customSizes[parseInt(size.slice(1))];
        var gp = gs ? parseFloat(gs.price) : NaN;
        return isNaN(gp) ? null : gp;
    }
    var inch = getInches();
    if (inch === null) return null;
    return Math.round(basePriceMini + Math.max(0, inch - 5) * PRICE_PER_INCH);
}

// Rough weight from diameter (6" ≈ 1 kg, scales with area) for sizes without an explicit weight.
function inchesToKg(inch) { return Math.round((inch * inch / 36) * 10) / 10; }

function sizeWeight() {
    var size = cakeSizeSelect.value;
    if (!size) return 0;
    if (size.charAt(0) === 'g') {
        var gs = _customSizes[parseInt(size.slice(1))];
        if (gs) {
            var w = parseFloat(gs.weight);
            if (!isNaN(w)) return w;
            var sn = parseFloat(String(gs.size).replace(/[^0-9.]/g, ''));
            if (!isNaN(sn)) return inchesToKg(sn);
        }
        return 0;
    }
    var inch = getInches();
    return inch === null ? 0 : inchesToKg(inch);
}

function recalcTotal() {
    var valueEl = document.getElementById('orderTotalValue');
    var hidden = document.getElementById('orderTotalHidden');
    if (!valueEl) return;
    var box = document.getElementById('orderTotal');
    var bdEl = document.getElementById('orderTotalBreakdown');
    var tr = translations[currentLang] || translations.en;
    var kg = tr['order.kg'] || 'kg';
    var priceReq = tr['cakes.priceAgreed'] || 'Price on request';

    var sizeSelected = !!cakeSizeSelect.value;
    var base = sizePrice();                 // may be null (size without its own base price)
    var weight = sizeWeight();              // kg (0 if unknown)
    var flavRate = selectedFlavourPrice || 0;
    var flavourCost = Math.round(flavRate * weight);
    var hasBase = base !== null;

    if (!sizeSelected || (!hasBase && !flavourCost)) {
        valueEl.textContent = sizeSelected ? priceReq : '—';
        if (hidden) hidden.value = '';
        if (box) box.style.display = sizeSelected ? '' : 'none';
        if (bdEl) bdEl.textContent = '';
        return;
    }

    var total = (hasBase ? base : 0) + flavourCost;
    valueEl.textContent = '€' + total;
    if (hidden) hidden.value = '€' + total;
    if (box) box.style.display = '';
    if (bdEl) {
        var parts = [];
        if (hasBase) parts.push('€' + base);
        if (flavourCost) parts.push('€' + flavRate + '/' + kg + ' × ' + weight + ' ' + kg + ' = €' + flavourCost);
        bdEl.textContent = parts.join('  +  ');
    }
}

