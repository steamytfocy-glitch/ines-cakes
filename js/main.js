let currentLang = localStorage.getItem('ines-lang') || 'en';

var langAnimating = false;

function applyTranslations(lang) {
    var t = translations[lang];
    if (!t) return;

    document.querySelectorAll('[data-i18n]').forEach(function(el) {
        var key = el.getAttribute('data-i18n');
        if (t[key]) {
            if (el.tagName === 'OPTION') {
                el.textContent = t[key];
            } else {
                el.innerHTML = t[key];
            }
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
        var key = el.getAttribute('data-i18n-placeholder');
        if (t[key]) {
            el.placeholder = t[key];
        }
    });

    if (document.getElementById('maintenanceOverlay') && typeof renderMaintenance === 'function') {
        renderMaintenance();
    }
    if (typeof renderWorkCategories === 'function') {
        renderWorkCategories();
    }
    if (typeof populateCustomSizes === 'function') {
        populateCustomSizes(_customSizes);
    }
    if (typeof recalcTotal === 'function') {
        recalcTotal();
    }
}

function setLanguage(lang, animate) {
    if (langAnimating) return;
    currentLang = lang;
    localStorage.setItem('ines-lang', lang);
    document.documentElement.lang = lang === 'ua' ? 'uk' : lang;

    document.querySelectorAll('.lang-btn').forEach(function(btn) {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    if (animate) {
        langAnimating = true;
        document.body.classList.add('lang-fade-out');

        setTimeout(function() {
            applyTranslations(lang);
            document.body.classList.remove('lang-fade-out');
            document.body.classList.add('lang-fade-in');

            setTimeout(function() {
                document.body.classList.remove('lang-fade-in');
                langAnimating = false;
            }, 350);
        }, 300);
    } else {
        applyTranslations(lang);
        if (typeof populateDatePicker === 'function') populateDatePicker();
    }
}

document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.addEventListener('click', function() {
        setLanguage(this.dataset.lang, true);
    });
});

