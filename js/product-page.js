// A cake uses its own sizes if it has any, otherwise the global default sizes.
function effectiveSizes() {
    return (product && product.sizes && product.sizes.length) ? product.sizes : _defaultSizes;
}

// Sort sizes by their numeric inch value (6, 7, 8 …) so dropdowns are in order.
function sortSizes(arr) {
    if (!Array.isArray(arr)) return arr;
    return arr.slice().sort(function(a, b) {
        var na = parseFloat(String((a && a.size) || '').replace(/[^0-9.]/g, ''));
        var nb = parseFloat(String((b && b.size) || '').replace(/[^0-9.]/g, ''));
        if (isNaN(na)) na = Infinity;
        if (isNaN(nb)) nb = Infinity;
        return na - nb;
    });
}

function t(k) { var tr = translations[currentLang] || translations.en; return tr[k] || translations.en[k] || k; }

function escapeHtml(str) {
    if (!str) return '';
    var d = document.createElement('div'); d.textContent = str; return d.innerHTML;
}

function locName(o) {
    if (!o) return '';
    if (currentLang === 'ua' && o.name_ua) return o.name_ua;
    if (currentLang === 'ru' && o.name_ru) return o.name_ru;
    return o.name || '';
}
function locDesc(o) {
    if (!o) return '';
    if (currentLang === 'ua' && o.desc_ua) return o.desc_ua;
    if (currentLang === 'ru' && o.desc_ru) return o.desc_ru;
    return o.desc || '';
}

function applyI18n() {
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
        var k = el.getAttribute('data-i18n');
        if (translations[currentLang] && translations[currentLang][k]) el.innerHTML = translations[currentLang][k];
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
        var k = el.getAttribute('data-i18n-placeholder');
        if (translations[currentLang] && translations[currentLang][k]) el.placeholder = translations[currentLang][k];
    });
}

function setLang(lang) {
    currentLang = lang;
    localStorage.setItem('ines-lang', lang);
    document.querySelectorAll('.lang-btn').forEach(function(b) { b.classList.toggle('active', b.dataset.lang === lang); });
    applyI18n();
    if (product) renderProduct();
}
document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.addEventListener('click', function() { setLang(this.dataset.lang); });
});

